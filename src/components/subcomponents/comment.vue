<template>
  <div class="cmt-container">
    <h3>发表评论</h3>
    <hr />
    <textarea
      placeholder="请输入评论内容（最多120字）"
      maxlength="120"
    ></textarea>
    <mt-button type="primary" size="large" @click="postComment"
      >发表评论</mt-button
    >

    <div class="cmt-list">
      <div class="cmt-item">
        <div class="cmt-title">
          第1楼&nbsp;&nbsp;用户：匿名用户&nbsp;&nbsp;发表时间：2012-12-12
          12:12:12
        </div>
        <div class="cmt-body">
          锄禾日当午
        </div>
      </div>
      <div class="cmt-item">
        <div class="cmt-title">
          第1楼&nbsp;&nbsp;用户：匿名用户&nbsp;&nbsp;发表时间：2012-12-12
          12:12:12
        </div>
        <div class="cmt-body">
          锄禾日当午
        </div>
      </div>
      <div class="cmt-item">
        <div class="cmt-title">
          第1楼&nbsp;&nbsp;用户：匿名用户&nbsp;&nbsp;发表时间：2012-12-12
          12:12:12
        </div>
        <div class="cmt-body">
          锄禾日当午
        </div>
      </div>
    </div>

    <mt-button type="danger" size="large" plain @click="getMore()"
      >加载更多</mt-button
    >
  </div>
</template>

<script>
import { Toast } from 'mint-ui'
export default {
  data() {
    return {
      pageIndex: 1, //默认展示第一页数据
      comments: []
    }
  },
  created() {
    this.getComments()
  },
  methods: {
    getComments() {
      this.$http
        .get('api/getcomments/' + this.id + '?pageindex=' + this.pageIndex)
        .then(result => {
          if (result.body.status === 0) {
            // this.comments = result.body.message
            //不请空老数据，应该用拼接
            this.comments = this.comments.concat(result.body.message)
          } else {
            Toast('获取评论失败！')
          }
        })
    },
    getMore() {
      this.pageIndex++
      this.getComments()
    },
    postComment() {
      if (this.msg.trim().length === 0) {
        return Toast('评论内容不能为空！')
      }
      //发表评论
      //参数1：请求URL地址
      //参数2：提交给服务器的数据对象{content：this.msg}
      //参数3：定义提交时候，表单中的数据{emulateJSON:true}
      this.$http
        .post('api/postcomment/' + this.$route.params.id, {
          content: this.msg.trim()
        })
        .then(function(result) {
          if (result.body.status === 0) {
            var cmt = {
              user_name: '匿名用户',
              add_time: Date.now(),
              content: this.msg.trim()
            }
            this.comments.unshift(cmt)
            this.msg = ''
          }
        })
    }
  },
  props: ['id']
}
</script>

